package com.address;

public class Address {

    String firstName;
    String lastName;
    String city;
    String address;
    String email;
    String mobile;
    int zip;
    Address(String firstName, String lastName, String city, String address, String email, String mobile, int zip)
    {
        this.firstName=firstName;
        this.lastName=lastName;
        this.city=city;
        this.address=address;
        this.email=email;
        this.mobile=mobile;
        this.zip=zip;
    }
}
